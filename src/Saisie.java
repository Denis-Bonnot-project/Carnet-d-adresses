    private void photoLblMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_photoLblMouseReleased
        // Créer un objet de type File
        File fichierPhoto=null;
        // Créer un composant JFileChooser
        JFileChooser fichierArechercher=new JFileChooser(fichierPhoto);
        // Ouvrir une boite de dialogue qui permet la recherche du fichier
        int etatRetour=fichierArechercher.showOpenDialog(Saisie.this);
       
        if (etatRetour==JFileChooser.APPROVE_OPTION){
            // Récupérer le fichier sélectionné
            fichierPhoto=fichierArechercher.getSelectedFile();
            // Stocker le chemin d'accès
            String url = fichierPhoto.toString();
            // Afficher la photo
            setPhoto(url);
        }
    }//GEN-LAST:event_photoLblMouseReleased

