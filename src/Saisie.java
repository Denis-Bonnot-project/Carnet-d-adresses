    private void ecrireLeContact(String tmptxt) throws IOException{
        // Lire le fichier listeContacts.txt et mémoriser le contenu dans une liste
        lireLesContacts();
        // Enregistrer le nouveau contact à la fin de la liste
        listeContacts.add(tmptxt);
        // Ecrire l'intégralité de la liste dans le fichier listeContacts.txt
        ecrireLesContacts();
    }

    public void supprimer(int tmpnum) throws IOException{
        // Lire le fichier listeContacts.txt et mémoriser le contenu dans une liste
        lireLesContacts();
        // Supprimer le contact positionné à l'indice tmpnum
        listeContacts.remove(tmpnum);
        // Ecrire l'intégralité de la liste dans le fichier listeContacts.txt
        ecrireLesContacts();
    }

    private void ecrireLesContacts() throws IOException{
         FichierAdresses f=new FichierAdresses();
         f.ouvrirEnEcriture("../donnees/listeContacts.txt");
         for (Object e : listeContacts){
             f.ecrire((String) e);
         }
         f.fermer();
    }
    
    private void lireLesContacts() throws IOException{
        listeContacts=new ArrayList();
        FichierAdresses f=new FichierAdresses();
        f.ouvrirEnLecture("../donnees/listeContacts.txt");
        String chaine= null;
        do{
            chaine=f.lire();
            if (chaine!=null) listeContacts.add(chaine);
        }
        while(chaine!=null);
        f.fermer();
    }
    
